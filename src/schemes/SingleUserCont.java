    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int firstValue = 0;
        int secondValue = 100;
        firstValue = jProgressBar1.getValue();
        secondValue = jProgressBar2.getValue();
        if (secondValue != 0) {
            int job = Integer.parseInt(jTextField1.getText());
            if (job <= secondValue) {

                //
                firstValue = firstValue + job;
                jProgressBar1.setValue(firstValue);

                TimerTask timerTask = new TimerTask(jProgressBar1, 3);
                timerTask.execute();

                //
                secondValue = secondValue - job;
                jProgressBar2.setValue(secondValue);
            } else {
                JOptionPane.showMessageDialog(null, "Too Large Job");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Memory Empty");
        }

    }//GEN-LAST:event_jButton1ActionPerformed

