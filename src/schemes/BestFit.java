    public void load(){
        int job = Integer.parseInt(jTextField1.getText());
        
        if (job>0 && job<31 && blocks[2]!=0) {
            jProgressBar4.setValue(job);
            blocks[2] = 0;
        }else if(job>0 && job<51 && blocks[1]!=0){
            jProgressBar3.setValue(job);
            blocks[1] = 0;
        }else if(job>0 && job<101 && blocks[0]!=0){
            jProgressBar2.setValue(job);
            blocks[0] = 0;
        }else{
            JOptionPane.showMessageDialog(null, "No Block Available");
        }
    }
    

